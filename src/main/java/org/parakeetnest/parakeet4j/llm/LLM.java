package org.parakeetnest.parakeet4j.llm;

public class LLM {
    private String name;

    private String url;

    // Getters and Setters
    public String getName() {
        return name;
    }

    public LLM setName(String name) {
        this.name = name;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public LLM setUrl(String url) {
        this.url = url;
        return this;
    }
}


